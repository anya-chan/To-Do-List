//define a class to store and get instances of to do list
public class ToDoList {
    private int id;
    private String item;
    private String status;
    
    public ToDoList(int id, String item, String status) {
        this.id = id;
        this.item = item;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
